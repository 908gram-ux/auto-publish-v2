    private static function calcStructure($contentMin) {
        if ($contentMin >= 10000) return ['h2_count' => '10~12', 'para_per_h2' => '4~6', 'sent_per_para' => '5~7', 'label' => '장문'];
        if ($contentMin >= 8000) return ['h2_count' => '8~10', 'para_per_h2' => '4~5', 'sent_per_para' => '4~6', 'label' => '중장문'];
        if ($contentMin >= 6000) return ['h2_count' => '7~9', 'para_per_h2' => '3~5', 'sent_per_para' => '4~5', 'label' => '중문+'];
        if ($contentMin >= 4000) return ['h2_count' => '5~7', 'para_per_h2' => '3~4', 'sent_per_para' => '3~5', 'label' => '중문'];
        return ['h2_count' => '4~6', 'para_per_h2' => '2~3', 'sent_per_para' => '3~4', 'label' => '단문'];
    }

